ORG 0000H

MOV 50H, #0AH
MOV 51H, #04H

MOV A, 50H
MOV R0, A

MOV A, 51H
MOV R1, A

COMPARE:
MOV A, R0
JZ CHECK_J

MOV A, R1
JZ H_GREATER

DEC R0
DEC R1
SJMP COMPARE

CHECK_J:
MOV A, R1
JZ EQUAL
SJMP J_GREATER

H_GREATER:
MOV 52H, #01H
SJMP END

J_GREATER:
MOV 52H, #0FFH
SJMP END

EQUAL:
MOV 52H, #00H

END:
SJMP END
END
